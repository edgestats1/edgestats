import type { DataCompleteness, LoadedMasterModel, TeamMatchProfile } from './types.js';
import { ratingGap, round } from './teamRatings.js';

export interface ConfidenceInput {
  home: TeamMatchProfile;
  away: TeamMatchProfile;
  model: LoadedMasterModel;
  probabilities: { homeWin: number; draw: number; awayWin: number };
  pairingNote?: string | null;
}

export interface ConfidenceResult {
  score: number;
  factors: string[];
  limitations: string[];
}

function completenessScore(profile: DataCompleteness): number {
  let score = 0;
  if (profile.hasPowerRating) score += 1;
  if (profile.hasTournamentTotals) score += 0.75;
  if (profile.hasCornerTrends) score += 0.5;
  if (profile.hasCardTrends) score += 0.5;
  if (profile.hasGoalkeeper) score += 0.5;
  if (profile.hasWorldCupPlayerStats) score += 0.5;
  if (profile.worldCupMatchesPlayed != null && profile.worldCupMatchesPlayed >= 3) score += 0.75;
  return score;
}

export function computeConfidence(input: ConfidenceInput): ConfidenceResult {
  const { home, away, model, probabilities, pairingNote } = input;
  const factors: string[] = [];
  const limitations: string[] = [];

  let score = 4.5;

  const homeComplete = completenessScore(home.dataCompleteness);
  const awayComplete = completenessScore(away.dataCompleteness);
  const completenessAvg = (homeComplete + awayComplete) / 2;
  score += Math.min(2.2, completenessAvg * 0.45);
  factors.push(`Data completeness index ${round(completenessAvg, 2)}`);

  const gap = ratingGap(home, away);
  if (gap != null) {
    const gapBoost = Math.min(1.4, Math.abs(gap) / 18);
    score += gapBoost;
    factors.push(`Power rating gap ${round(Math.abs(gap), 1)}`);
  } else {
    score -= 0.5;
    limitations.push('Power rating gap unavailable for one or both teams.');
  }

  const favouriteProb = Math.max(probabilities.homeWin, probabilities.draw, probabilities.awayWin);
  if (favouriteProb >= 0.55) {
    score += 0.6;
    factors.push(`Clear favourite at ${round(favouriteProb * 100, 1)}% outcome probability`);
  } else if (favouriteProb <= 0.4) {
    score -= 0.4;
    factors.push('Tight three-way outcome split lowers certainty');
  }

  if (model.tournamentGoalsPerTeam != null) {
    score += 0.3;
  } else {
    limitations.push('Tournament scoring baseline unavailable; using fallback league average.');
  }

  if (!home.dataCompleteness.hasGoalkeeper || !away.dataCompleteness.hasGoalkeeper) {
    score -= 0.4;
    limitations.push('Goalkeeper save projection may be limited without full GK export data.');
  }

  if (!home.dataCompleteness.hasWorldCupPlayerStats || !away.dataCompleteness.hasWorldCupPlayerStats) {
    limitations.push('Key player threats may rely on club-season data where World Cup player stats are missing.');
  }

  if (pairingNote) {
    score -= 0.8;
    limitations.push(pairingNote);
  }

  // Model metadata documents no legacy confidence model in codebase.
  limitations.push(
    'Confidence is generated by the EdgeStats prediction-engine heuristic (not a previously shipped /10 model).',
  );

  score = Math.max(2.5, Math.min(9.5, score));

  return {
    score: round(score, 1) ?? 5,
    factors,
    limitations,
  };
}
